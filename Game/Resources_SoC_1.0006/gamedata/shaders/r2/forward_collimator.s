-- shadow map rendering --
function l_point		(shader, t_base, t_second, t_detail)
	shader:begin	("shadow_direct_model","shadow_direct_base")
			: fog		(false)
			: blend		(false,blend.zero,blend.one)
			: aref 		(false,2)
			: sorting	(1, false)
			: zb 		(true,true)
	shader:sampler	("s_base")      :texture	(t_base)
end
