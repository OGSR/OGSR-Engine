function normal                (shader, t_base, t_second, t_detail)
  shader:begin                	("hud_scope","hud_scope")
        : sorting       		(1, false)
        : blend                	(true,blend.srcalpha,blend.invsrcalpha)
		: aref					(false, 0)	
        : zb                	(false,false)
        : distort        		(false)
        : fog                	(false)
  shader:sampler        ("s_base")       :texture  (t_base): clamp()
end